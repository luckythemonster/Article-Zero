// build-unmounted-manifest.mjs — walks "unmounted assets/" and emits a flat
// list of every file found, so the SpriteGallery overlay can render an
// inventory sidebar of what still needs to be plugged into the pipeline.
//
// Run with: npm run unmounted (also chained into npm run art).

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const UNMOUNTED_DIR = path.join(ROOT, "unmounted assets");
const OUT_TS = path.join(ROOT, "src/data/unmounted.generated.ts");

async function walk(dir, rootDir, acc) {
  let entries;
  try {
    entries = await fs.readdir(dir, { withFileTypes: true });
  } catch {
    return acc;
  }
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      await walk(full, rootDir, acc);
    } else if (entry.isFile()) {
      const stat = await fs.stat(full);
      const rel = path.relative(rootDir, full).split(path.sep).join("/");
      const ext = path.extname(entry.name).slice(1).toLowerCase();
      acc.push({ path: rel, sizeBytes: stat.size, ext });
    }
  }
  return acc;
}

async function main() {
  const files = await walk(UNMOUNTED_DIR, UNMOUNTED_DIR, []);
  files.sort((a, b) => a.path.localeCompare(b.path));

  const body = `// AUTO-GENERATED by scripts/build-unmounted-manifest.mjs. Do not hand-edit.
// Run \`npm run unmounted\` to regenerate after changing files in "unmounted assets/".

export interface UnmountedFile {
  /** Path relative to the "unmounted assets/" directory. */
  path: string;
  sizeBytes: number;
  /** Lowercased extension without leading dot ("png", "zip", "json", ""). */
  ext: string;
}

export const UNMOUNTED_FILES: UnmountedFile[] = ${JSON.stringify(files, null, 2)};
`;

  await fs.writeFile(OUT_TS, body, "utf8");
  console.log(`Wrote ${files.length} entries to ${path.relative(ROOT, OUT_TS)}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
